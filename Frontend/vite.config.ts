// ============================================================
// vite.config.ts - Vite 构建工具配置
// 说明：配置开发服务器、构建、路径别名、插件等
//       这是前端工程化的核心配置文件
// ============================================================
import { defineConfig } from 'vite'      // Vite 的配置函数（带类型提示）
import vue from '@vitejs/plugin-vue'     // Vue3 单文件组件(.vue)编译插件
import { resolve } from 'path'           // Node.js 路径处理模块

// defineConfig 是 Vite 提供的辅助函数，能获得完整的 TypeScript 类型提示
export default defineConfig({
  // ============================================================
  // 插件配置
  // 拓展方式：新增 Vite 插件在此数组中添加
  // ============================================================
  plugins: [
    vue(),  // 启用 .vue 文件编译支持
  ],

  // ============================================================
  // 路径别名（需与 tsconfig.json 中的 paths 保持一致）
  // 好处：用 @/ 代替 ../../src/ 长路径
  // 使用示例：import Header from '@/components/AppHeader.vue'
  // ============================================================
  resolve: {
    alias: {
      '@': resolve(__dirname, 'src'),  // @ 指向 src 目录
    },
  },

  // ============================================================
  // 开发服务器配置
  // ============================================================
  server: {
    host: '0.0.0.0',                    // 监听所有网卡，允许外部IP访问
    port: 3000,                         // 前端端口
    open: false,                         // 启动后自动打开浏览器
    // proxy: 代理配置，将 /api 请求转发到后端（解决跨域）
    proxy: {
      '/sitemap.xml': {
        target: 'http://localhost:8080', // 后端地址
        changeOrigin: true,
      },
      '/api': {
        target: 'http://localhost:8080', // 后端地址
        changeOrigin: true,              // 修改请求头中的 origin
      },
    },
  },

  // ============================================================
  // 构建配置
  // ============================================================
  build: {
    outDir: 'dist',           // 打包输出目录
    sourcemap: false,         // 生产环境不生成 sourcemap（减小体积）
    // chunkSizeWarningLimit: 500, // 包体积警告阈值(kb)
  },
})
